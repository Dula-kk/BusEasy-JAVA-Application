     private void loadUsers() {
        List<User> users = UserController.getAllUsers();
        DefaultTableModel model = (DefaultTableModel) uTable.getModel();
        model.setRowCount(0); // Clear existing data
        for (User user : users) {
            model.addRow(new Object[]{
                user.getId(),
                user.getFname(),
                user.getLname(),
                user.getUsername(),
                user.getContact(),
                user.getRole()
            });
        }
    }

    private boolean validateInputs() {
        if ( fnameInput.getText().isEmpty() || lnameInput.getText().isEmpty() ||
            lnameInput.getText().isEmpty() || UsernameInput.getText().isEmpty() || passwordInput.getPassword().length == 0) {
            JOptionPane.showMessageDialog(this, "Please fill in all fields.", "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        return true;
    }

