    private void insertBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_insertBtnActionPerformed
        // TODO add your handling code here: 
       if (!validateInputs()) {
            return;
        }

        String fname = fnameInput.getText();
        String lname = lnameInput.getText();
        String username = UsernameInput.getText();
        String password = new String(passwordInput.getPassword());
        int contact = Integer.parseInt(contactInput.getText());
        String role = passengerOp.isSelected() ? "Passenger" : "Conductor";

        User user = new User(fname, lname, username, password, contact, role);
        if (UserController.addUser(user)) {
            JOptionPane.showMessageDialog(this, "User added successfully!");
            loadUsers();
            clearFields();
        } else {
            JOptionPane.showMessageDialog(this, "Failed to add user.");
        }

    }//GEN-LAST:event_insertBtnActionPerformed

    private void findBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_findBtnActionPerformed
        // TODO add your handling code here:
        
        String userIdText = userIdInput.getText();
        if (userIdText.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter a User ID.");
            return;
        }

        int userId = Integer.parseInt(userIdText);
        User user = UserController.findUserById(userId);
        if (user != null) {
            fnameInput.setText(user.getFname());
            lnameInput.setText(user.getLname());
            UsernameInput.setText(user.getUsername());
            contactInput.setText(String.valueOf(user.getContact()));
            passwordInput.setText(user.getPassword());
            if ("Passenger".equals(user.getRole())) {
                passengerOp.setSelected(true);
            } else {
                conductorOp.setSelected(true);
            }
        } else {
            JOptionPane.showMessageDialog(this, "User not found.");
        }
      
    }//GEN-LAST:event_findBtnActionPerformed

